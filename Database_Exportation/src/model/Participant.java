package model;

/**
 * A class represents the Participant object in PRML
 * @author kma
 *
 */
public class Participant {
	
	private int id;

	/**
	 * @param id
	 */
	public Participant(int id) {
		super();
		this.id = id;
	}

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	
	
}
